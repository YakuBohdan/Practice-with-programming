#include <stdio.h>

int main() {
    int n;
    printf("Enter the length of the sequences (n): ");
    scanf("%d", &n);

    int dp[10001]; // Масив для зберігання кількості допустимих послідовностей
    dp[0] = 1; // Порожня послідовність - 1 спосіб
    dp[1] = 2; // Послідовність "0" або "1" - 2 способи
    dp[2] = 3; // Послідовності "00", "01", "10" - 3 способи

    for (int i = 3; i <= n; i++) {
        dp[i] = dp[i-1] + dp[i-2]; // Рекурентне співвідношення: кількість допустимих послідовностей довжиною i
                                  // дорівнює сумі кількостей допустимих послідовностей довжиною (i-1) та (i-2)
    }

    int result = dp[n]; // Кількість допустимих послідовностей довжиною n
    printf("Number of sequences with length %d: %d\n", n, result);
scanf(" ");
    return 0;
}
